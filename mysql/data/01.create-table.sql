CREATE TABLE IF NOT EXISTS PELICULAS (
    ID_PELICULA VARCHAR(15) PRIMARY KEY,
    TITULO VARCHAR (100) NOT NULL,
    ESTRENO INT NOT NULL,
    DURACION INT NOT NULL,
    DESCRIPCION VARCHAR(500),
    CONSTRAINT FK_ID_SAGA FOREIGN KEY (ID_SAGA) REFERENCES SAGAS (ID_SAGA)
) ;

CREATE TABLE IF NOT EXISTS PELICULAS_CATEGORIAS(
    CONSTRAINT FK_ID_PELICULA FOREIGN KEY(ID_PELICULA) REFERENCES PELICULAS(ID_PELICULA),
    CONSTRAINT FK_ID_CATEGORIA FOREIGN KEY(ID_CATEGORIA) REFERENCES CATEGORIAS(ID_CATEGORIA) 
)

CREATE TABLE IF NOT EXISTS CATEGORIAS(
    ID_CATEGORIA VARCHAR(15) PRIMARY KEY,
    NOMBRE_CATEGORIA VARCHAR(100)
)


CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    password_hash VARCHAR(255) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status ENUM('active', 'inactive') DEFAULT 'active'
);