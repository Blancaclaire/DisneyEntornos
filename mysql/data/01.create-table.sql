-- La tabla contiene las sagas de peliculas de la plataforma
CREATE TABLE IF NOT EXISTS SAGAS(
    ID_SAGA VARCHAR(15) PRIMARY KEY,
    NOMBRE_SAGA VARCHAR(100),
    NUMERO_PELICULAS INT
);

-- La tabla contiene los datos acerca de las peliculas

CREATE TABLE IF NOT EXISTS PELICULAS (
    ID_PELICULA VARCHAR(15) PRIMARY KEY,
    TITULO VARCHAR (100) NOT NULL,
    ESTRENO INT NOT NULL,
    DURACION INT NOT NULL,
    DESCRIPCION VARCHAR(500),
    CONSTRAINT FK_ID_SAGA FOREIGN KEY (ID_SAGA) REFERENCES SAGAS (ID_SAGA)
) ;

 CREATE TABLE IF NOT EXISTS CATEGORIAS(
    ID_CATEGORIA VARCHAR(15) PRIMARY KEY,
    NOMBRE_CATEGORIA VARCHAR(50)
 );

 CREATE TABLE IF NOT EXISTS PELICULAS_CATEGORIAS (
    ID_PELICULA VARCHAR(15),
    ID_CATEGORIA VARCHAR(15),
    PRIMARY KEY (ID_PELICULA, ID_CATEGORIA),
    CONSTRAINT FK_PELICULA FOREIGN KEY (ID_PELICULA) REFERENCES PELICULAS(ID_PELICULA),
    CONSTRAINT FK_CATEGORIA FOREIGN KEY (ID_CATEGORIA) REFERENCES CATEGORIAS(ID_CATEGORIA)
);




